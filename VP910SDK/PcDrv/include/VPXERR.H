#ifndef __vpxerr_h__
#define __vpxerr_h__

/******************************************************************************
		システム・終了コード
******************************************************************************/
#define		ISPX_CORRECT				0	/* 正常終了                      */
#define		ISPX_ERROR					-1	/* エラー終了                    */

/******************************************************************************
		コマンド動的エラー（コマンドリの戻り値）
******************************************************************************/
#define		INVALID_LINE_DATA			-3	/* ハフ変換ライン検出エラー      */
#define		INVALID_CORR_AREA			-4	/* 相関テンプレート面積エラー    */
#define		INVALID_CORR_DATA			-5	/* 相関テンプレート分散値エラー  */
#define		INVALID_CAM_SETUP			-6	/* 映像入力セットアップエラー    */

/******************************************************************************
		エラーコード（IPSYSERRSTS構造体 - errorメンバ）
******************************************************************************/
#define		CMD_COMPLETE				0		/* 正常終了                  */
#define		CMD_ERROR					0xFE	/* コマンドエラー            */
#define		CMD_EXCEPT					0xFF	/* オンボードCPU例外         */

#endif /* !__vpxerr_h__ */

